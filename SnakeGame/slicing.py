char = ["a", "b", "c", "d", "e", "f", "g"]

# Below methods can be used in python list and tuples
print(char[2:5])
print(char[2:])
print(char[:5])
print(char[2:5:2])
print(char[::2])
# To reverse the list
print(char[::-1])
print(char[::-2])

# It's going to print only characters from index 2 to index 4 not including the last index
